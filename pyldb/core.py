# -*- coding: utf-8 -*-
"""A perceived loudness (PLdB) calculator.

PyLdB implements Stevens' Mark VII procedure for the calculation of the
perceived loudness of a pressure signature.

Classes
-------
PyLdB
    Object-oriented calculator for importing, preprocessing, analyzing, and
    post-processing pressure signatures.
LoudnessResults
    Container returned by :meth:`PyLdB.calculate` and exposed as
    ``PyLdB.loudness``.

Routine Listings
----------------
PyLdB.calculate(...)
    Calculates PLdB and acoustic post-processing metrics, then stores them on
    ``object.loudness``.
PyLdB.perceived_loudness(...)
    Calculates perceived loudness in PLdB using Stevens' Mark VII procedure.
PyLdB.import_signature(filename[, header_lines, delimiter])
    Imports a two-column time and pressure signature from a file.
PyLdB.window(len_window)
    Applies a Hanning window to the active pressure signature.
PyLdB.padding(n_pad_points)
    Applies zero padding to the front and rear of the active signature.
PyLdB._power_spectrum()
    Performs an FFT on the active pressure signature and generates a one-sided
    power spectrum.
PyLdB._power_interp(freq, power)
    Interpolates power data to match frequency band boundaries.
PyLdB._sound_pressure_levels(freq, power)
    Calculates the sound pressure level of each frequency band.
PyLdB._equivalent_loudness(loudness)
    Calculates equivalent loudness values.
PyLdB._calc_total_loudness(L_eq)
    Uses table values to look up and interpolate a summation factor and sone
    values to calculate total loudness for a signature.

See Also
--------
numpy.interp : Linear interpolation for arrays.
numpy.hanning : Calculates a Hanning window given a number of points.
numpy.pad : Zero-pads an array.
numpy.fft.rfft : Real-input fast Fourier transform (FFT) routine.
numpy.fft.rfftfreq : Generates the one-sided frequency range for an FFT.
numpy.argsort : Returns indices to sort an array.
numpy.nonzero : Returns non-zero elements in an array subject to conditions.
numpy.trapz : Performs trapezoidal numerical integration.

Notes
-----
The pyldb module utilizes the Mark VII procedure for the calculation of
perceived loudness created by Stevens. The procedure works by first importing
time and pressure data, which is windowed and zero-padded prior to performing
a frequency analysis via an FFT. From the frequency analysis, a one-sided
power spectrum is created, which allows for the calculation of the energy
contained in one-third octave frequency bands for the pressure signature. The
energy is found by integrating the power spectrum using a trapezoidal rule.
This energy can be transformed into a sound pressure level measured in decibels
for each frequency band. The sound pressure levels are then converted to an
equivalent loudness value using equal-sone contours generated by Stevens. Using
data tabulated by Stevens, a total loudness in units of sones is calculated,
which is then transformed into perceived loudness using a power law developed
by Stevens.

References
----------
Stevens, S., "Perceived level of noise by Mark VII and decibels (E),"
The Journal of the Acoustical Society of America, Vol. 51, No. 2B, 1972,
pp. 575-601.

Jackson, G., and Leventhall, H., "Calculation of the perceived level of noise
(PLdB) using Stevens' method (Mark VII)," Applied Acoustics, Vol. 6, No. 1,
1973, pp. 23-34.

Shepherd, K. P., and Sullivan, B. M., "A loudness calculation procedure
applied to shaped sonic booms," 1991.

Johnson, D., and Robinson, D., "Procedure for calculating the loudness of
sonic bangs," Acta Acustica united with Acustica, Vol. 21, No. 6, 1969,
pp. 307-318.

Examples
--------
>>> from pyldb import PyLdB
>>> calculator = PyLdB("misc/panair_r1.sig", header_lines=3)
>>> results = calculator.calculate(pad_front=6, pad_rear=6, len_window=800)
>>> print(results.pldb)
"""

from __future__ import annotations

