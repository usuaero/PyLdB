@dataclass
class LoudnessResults:
    """Container for calculated loudness metrics.

    Attributes
    ----------
    pldb : float
        Perceived loudness in PLdB calculated with Stevens' Mark VII method.
    ctl : float
        Fidell CTL high-annoyance response probability for the calculated PLdB.
        This is also available as ``ctl_response`` for clarity.
    community_tolerance_level : float or None
        Community Tolerance Level used to calculate ``ctl``. When ``a_star`` is
        supplied instead of ``ctl``, this value is derived from ``a_star`` and
        ``growth``.
    dnl : float
        Day-night average level calculated from the supplied day and night
        loudness values.
    equivalent_loudness : float
        Energy-equivalent loudness level calculated from the supplied loudness
        sequence.
    alphabet_weighted_loudnesses : dict
        Overall A-, B-, C-, and D-weighted levels keyed as ``"a"``, ``"b"``,
        ``"c"``, and ``"d"``.
    """

    pldb: float
    ctl: float
    community_tolerance_level: Optional[float]
    dnl: float
    equivalent_loudness: float
    alphabet_weighted_loudnesses: Dict[str, float]

    @property
    def ctl_response(self) -> float:
        return self.ctl

    @property
    def a_weighted_loudness(self) -> float:
        return self.alphabet_weighted_loudnesses["a"]

    @property
    def b_weighted_loudness(self) -> float:
        return self.alphabet_weighted_loudnesses["b"]

    @property
    def c_weighted_loudness(self) -> float:
        return self.alphabet_weighted_loudnesses["c"]

    @property
    def d_weighted_loudness(self) -> float:
        return self.alphabet_weighted_loudnesses["d"]


